A = float(input("Digite o valor de A "))
B = float(input("Digite o valor de B "))
C = float(input("Digite o valor de C "))
PI = 3.14159
Triangulo = (A*C)/2
print("Triângulo:", round(Triangulo, 3))
Circulo = PI*C**2
print("Círculo:", round(Circulo, 3))
Trapezio = ((A+B)*C)/2
print("Trapézio:", round(Trapezio, 3))
Quadrado = B*B
print("Quadrado:", round(Quadrado, 3))
Retangulo = A*B
print("Retângulo:", round(Retangulo, 3))
